public class usuario {
    private int  idCiudades;
    private int idroles;
    private int idusuarios;
    private String nombreUsuario;
    private String apellidoUsuario;
    private double telefonoUsuario;
    private String emailUsuario;
    private String direccionUsuario;
    private  String fechaNacimientoUsuario;
    private String contraseñaUsuario;
    public int getIdCiudades() {
        return idCiudades;
    }
    public void setIdCiudades(int idCiudades) {
        this.idCiudades = idCiudades;
    }
    public int getIdroles() {
        return idroles;
    }
    public void setIdroles(int idroles) {
        this.idroles = idroles;
    }
    public int getIdusuarios() {
        return idusuarios;
    }
    public void setIdusuarios(int idusuarios) {
        this.idusuarios = idusuarios;
    }
    public String getNombreUsuario() {
        return nombreUsuario;
    }
    public void setNombreUsuario(String nombreUsuario) {
        this.nombreUsuario = nombreUsuario;
    }
    public String getApellidoUsuario() {
        return apellidoUsuario;
    }
    public void setApellidoUsuario(String apellidoUsuario) {
        this.apellidoUsuario = apellidoUsuario;
    }
    public double getTelefonoUsuario() {
        return telefonoUsuario;
    }
    public void setTelefonoUsuario(double telefonoUsuario) {
        this.telefonoUsuario = telefonoUsuario;
    }
    public String getEmailUsuario() {
        return emailUsuario;
    }
    public void setEmailUsuario(String emailUsuario) {
        this.emailUsuario = emailUsuario;
    }
    public String getDireccionUsuario() {
        return direccionUsuario;
    }
    public void setDireccionUsuario(String direccionUsuario) {
        this.direccionUsuario = direccionUsuario;
    }
    public String getFechaNacimientoUsuario() {
        return fechaNacimientoUsuario;
    }
    public void setFechaNacimientoUsuario(String fechaNacimientoUsuario) {
        this.fechaNacimientoUsuario = fechaNacimientoUsuario;
    }
    public String getContraseñaUsuario() {
        return contraseñaUsuario;
    }
    public void setContraseñaUsuario(String contraseñaUsuario) {
        this.contraseñaUsuario = contraseñaUsuario;
    }


}
