abstract class Ciudades {
    private int idCiudades;
    private String nomCiudades;
    public int getIdCiudades() {
        return idCiudades;
    }
    public void setIdCiudades(int idCiudades) {
        this.idCiudades = idCiudades;
    }
    public String getNomCiudades() {
        return nomCiudades;
    }
    public void setNomCiudades(String nomCiudades) {
        this.nomCiudades = nomCiudades;
    }

}