public class proveedor {
    private int idProveedor;
    private String nombreProveedor;
    private String direccionProveddor;
    private double telefonoProveedor;
    public int getIdProveedor() {
        return idProveedor;
    }
    public void setIdProveedor(int idProveedor) {
        this.idProveedor = idProveedor;
    }
    public String getNombreProveedor() {
        return nombreProveedor;
    }
    public void setNombreProveedor(String nombreProveedor) {
        this.nombreProveedor = nombreProveedor;
    }
    public String getDireccionProveddor() {
        return direccionProveddor;
    }
    public void setDireccionProveddor(String direccionProveddor) {
        this.direccionProveddor = direccionProveddor;
    }
    public double getTelefonoProveedor() {
        return telefonoProveedor;
    }
    public void setTelefonoProveedor(double telefonoProveedor) {
        this.telefonoProveedor = telefonoProveedor;
    }
}
